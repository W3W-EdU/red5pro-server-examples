package com.red5pro.server.cauldron.facemask;

public class ModuleConfig {
	
	private String[] supportLibs;
	private String moduleFile;
	public String[] getSupportLibs() {
		return supportLibs;
	}
	public void setSupportLibs(String[] supportLibs) {
		this.supportLibs = supportLibs;
	}
	public String getModuleFile() {
		return moduleFile;
	}
	public void setModuleFile(String moduleFile) {
		this.moduleFile = moduleFile;
	}	
}
